!--------------------------------------------------------------
! Subrutina para calcular el Loop relativista
! empleando un cutoff qmax: <<Meson-meson interactions
! in a nonperturbative chiral approach
! [Phys. Rev. D 59, 074001 (1999)]>>
! I=Int4*1/(4pi)⁴*1/[(q²-m1²+I*e)((P-q)²-m2²+I*e)]
!--------------------------------------------------------------
! Name: CLoopRA
!   C: Cutoff
!   Loop:Loop
!   R: relativista
!   A: Analítica
!--------------------------------------------------------------
! s: energy² in which we calculate (MeV²)
! qmax: cutoff in MeVs
! xm1: mass which goes alone in first factor on 
! the denominator.
! xm2: mass which goes with P (s) in the denominator
! cRes: the result
      subroutine CLoopRA(s,q,m1,m2,cRes)
      implicit none
      real*8 m1,m2,q
      complex*16 Delt,xm1,xm2,qmax,pi
      complex*16 cres
      complex*16 s,nu
      pi=dcmplx(4.0d0*atan(1.0d0),0.0d0)
      xm1 = dcmplx(m1,0.0d0)
      xm2 = dcmplx(m2,0.0d0)
      qmax= dcmplx(q ,0.0d0)
      nu   = sqrt((s-(xm1+xm2)**2)*(s-(xm1-xm2)**2))
      Delt = xm2**2-xm1**2
      cRes = dcmplx(1.0d0/32.0d0,0.0d0)/pi**2*(
     & -Delt/s*log(xm1**2/xm2**2)+
     & nu/s*(log(  s-Delt+nu*sqrt(1.0d0+xm1**2/qmax**2))
     &       -log(-s+Delt+nu*sqrt(1.0d0+xm1**2/qmax**2))
     &       +log( s+Delt+nu*sqrt(1.0d0+xm2**2/qmax**2))
     &       -log(-s-Delt+nu*sqrt(1.0d0+xm2**2/qmax**2)))
     &+2.0d0*Delt/s*log((1.0d0+sqrt(1.0d0+xm1**2/qmax**2))/
     &                  (1.0d0+sqrt(1.0d0+xm2**2/qmax**2)))
     &-2.0d0*log(1.0d0+sqrt(1.0d0+xm1**2/qmax**2)) 
     &-2.0d0*log(1.0d0+sqrt(1.0d0+xm2**2/qmax**2)) 
     &+log(xm1**2*xm2**2/qmax**4))
      return
      end

