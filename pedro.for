      subroutine gfvol(e,m1,m2,lfv,submu,gfv)
CF2PY INTENT(OUT) :: gfv
! This subroutine gives you the finite volume
! loop function renormalized using dimensional
! regularization
!--------------------------------------------------------------
! e: com energy (MeV)
! alf: substraction constant
! m1: mass 
! m2: mass 
! xmu: mass scale
! lfv: side length of the box (Volume=L^3) [in MeV^{-1}]
! gfv: the result
      implicit none
      integer in,jtotal,nfv
      real*8 s,e,submu,qmax,number
      real*8 m1,m2,lamb,lfv,q2,q,pi,gfv
      real*8 wb,wk,k2,Om,gg,alf
      complex*16 res,diffg,diffgs           
      s = e**2
      pi = 4.0d0*atan(1.0d0)
      ! The loop function using dimensional 
      ! regularization
c      call drloopra(dcmplx(s),alf,mu,m1,m2,res)
c      call Gloop(dcmplx(s),m1,m2,submu,res)
      diffgs = dcmplx(0.0d0,0.0d0)
      number = 200.0d0
      ! here we take the average of the difference
      ! (Eq:1)   diffg = G_fv(s,qmax) - G(s,qmax)
      ! for "number" of cutoffs <<qmax>>
      ! ranging from 1 GeV to 4 GeV
      do in = 1,int(number)
      qmax = 1.d3 + dble(in-1)/(number-1.0d0)*3.d3
      ! here I call the finite volume difference (Eq:1)
      ! for each cutoff "qmax"
      call gfvaux(e,m1,m2,lfv,qmax,diffg)
      ! I accumulate the value
      diffgs = diffgs + diffg
      end do
      ! I take the average
      diffg = diffgs/number
      ! The result is the DR loop function
      ! plus the finite volume correction
      ! which is given by the averaged
      ! term diffg
      gfv =dreal(2.d0*m2*diffg)
      end subroutine 
      
      subroutine gfvaux(e,m1,m2,lfv,qmax,gfv)
      implicit none
      integer in,jtotal,nfv
      real*8 e,lamb,m1,m2,qmax,qmaxnew
      real*8 lfv,q2,q,pi,mb1,mk1
      real*8 wb,wk,k2,Om,gg
      complex*16 cres,gfv,s
      integer, parameter :: nfvmax = 1000
!       real*8,dimension(0:3*nfvmax**2):: mult2
      real*8 mult2
      s = dcmplx(e,0.0d0)**2
      mb1 =  m1
      mk1 =  m2
      pi = 4.0d0*atan(1.0d0)

      nfv = int( (lfv*qmax/(2.0d0*pi))**2)
      qmaxnew = sqrt(dble(nfv))*2.0d0*pi/lfv
      if (nfv.gt.nfvmax) then
c      write(*,*) 'nfvmax not enough, should be >=',nfv
c      write(*,*) 'for qmax = ',qmax
c      write(*,*) '[The parameter nfvmax is the maximum modulus^2   '
c      write(*,*) ' of the vector \vec{n} = (i,j,k)                 '
c      write(*,*) ' entering the discrete sum. Hence,               '
c      write(*,*) ' is the maximum input value entering             '
c      write(*,*) ' the multiplicity. It may be necessary           '
c      write(*,*) ' to enlarge it if you want to sum                '
c      write(*,*) ' for larger cutoffs, or in large sizes.          '
c      write(*,*) ' In that case,                                   '
c      write(*,*) ' you also have to modify the function mult2      '
c      write(*,*) ' adding the required multiplicities.]            '
c      stop
      nfv=nfvmax
      end if

      gg=0.0d0
      do in = 0,nfv
      q2 = dble(in)*(2.0d0*pi/lfv)**2
      q  = sqrt(q2)
      wb = sqrt(mb1**2+q2)
      wk = sqrt(mk1**2+q2)
      Om = (wb+wk)/2.0d0/wb/wk
      gg = gg + 1.0d0/lfv**3*mult2(in)/(e**2-(wb+wk)**2)*Om
      end do
      gfv = dcmplx(gg,0.0d0)
      call CLoopRA(s,qmax,mb1,mk1,cres)
      gfv = gfv-cres
      end subroutine

!--------------------------------------------------------------
! Subrutina para calcular el Loop relativista
! mediante regularización dimensional
! I=Int4*1/(4pi)⁴*1/[(q²-m1²+I*e)((P-q)²-m2²+I*e)]
! e.g. in <<scalar, axial-vector, and tensor resonances from de 
! rho D star, omega D star interaction in the hidden gauge for-
! malism [Phys. Rev. D 80, 014025 (2009)]>>
!--------------------------------------------------------------
! Name: DRLoopRA
!   DR: dimensional regularization
!   Loop:Loop
!   R: relativista
!   A: Analítica
!--------------------------------------------------------------
! s: energy² in which we calculate (MeV²)
! alf: substraction constant
! xm1: mass which goes alone in first factor on 
! the denominator.
! xm2: mass which goes with P (s) in the denominator
! xmu: mass scale
! cRes: the result
      subroutine DRLoopRA(ss,alf,xmu,xm1,xm2,cRes)
      IMPLICIT NONE
      COMPLEX*16 s,ss,cp2,cp,c1,c2,c3,cres,SQS
      REAL*8 xmu,xm1,xm2,pi,alf
      s=ss
      SQS=SQRT(S)
      pi=4.0d0*atan(1.0d0)
      cp2=(s-(xm1+xm2)**2)*(s-(xm1-xm2)**2)/4.0d0/s
      cp=sqrt(cp2)
      IF (DIMAG(cp).LT.0.0d0) cp=-cp
      IF (DIMAG(cp).LT.0.0d0) sqs=-sqs
      C1=dcmplx(alf+log(xm1**2/xmu**2),0.0d0)
      C2=(xm2**2-xm1**2+s)/(2.0d0*s)*log(xm2**2/xm1**2)
      C3=cp/sqs*(log(+s-xm2**2+xm1**2+2.0d0*cp*sqs)-
     &               log(-s+xm2**2-xm1**2+2.0d0*cp*sqs)+
     &               log(+s+xm2**2-xm1**2+2.0d0*cp*sqs)-
     &               log(-s-xm2**2+xm1**2+2.0d0*cp*sqs) )
      cRes=1.0d0/16.0d0/pi**2*(C1+C2+C3)
      return
      end

